/*! @file
 *  This file contains an ISA-portable PIN tool for tracing instructions
 */

#include "pin.H"
#include <iostream>
#include <fstream>
#include <mutex>




/* ===================================================================== */
/* Global Variables */
/* ===================================================================== */

std::mutex writeMutex;
std::ofstream TraceFile;
UINT32 count_trace = 0; // current trace number

/* ===================================================================== */
/* Commandline Switches */
/* ===================================================================== */

KNOB< std::string > KnobOutputFile(KNOB_MODE_WRITEONCE, "pintool", "o", "trace.out", "specify trace file name");
KNOB< BOOL > KnobNoCompress(KNOB_MODE_WRITEONCE, "pintool", "no_compress", "0", "Do not compress");

/* ===================================================================== */
/* Print Help Message                                                    */
/* ===================================================================== */

INT32 Usage()
{
    std::cerr << "This tool produces a compressed (dynamic) instruction trace.\n"
            "The trace is still in textual form but repeated sequences\n"
            "of the same code are abbreviated with a number which dramatically\n"
            "reduces the output size and the overhead of the tool.\n"
            "\n";

    std::cerr << KNOB_BASE::StringKnobSummary();

    std::cerr << std::endl;

    return -1;
}

/* ===================================================================== */
VOID docount(const std::string* s)
{
    const std::lock_guard< std::mutex > lock(writeMutex);
    TraceFile.write(s->c_str(), s->size());
}

/* ===================================================================== */

VOID Trace(TRACE trace, VOID* v)
{
    for (BBL bbl = TRACE_BblHead(trace); BBL_Valid(bbl); bbl = BBL_Next(bbl))
    {
        std::string traceString = "";

        for (INS ins = BBL_InsHead(bbl); INS_Valid(ins); ins = INS_Next(ins))
        {
            traceString += "%" + INS_Disassemble(ins) + "\n";
        }

        // we try to keep the overhead small
        // so we only insert a call where control flow may leave the current trace

        if (KnobNoCompress)
        {
            INS_InsertCall(BBL_InsTail(bbl), IPOINT_BEFORE, MAKE_AFUNPTR(docount), IARG_PTR, new std::string(traceString),
                           IARG_END);
        }
        else
        {
            // Identify traces with an id
            count_trace++;

            {
                // Write the actual trace once at instrumentation time.
                // All writes must be protected also inside the instrumentation callback to prevent a race
                // against other threads that write to the file from an analysis routine.
                const std::lock_guard< std::mutex > lock(writeMutex);
                std::string m = "@" + decstr(count_trace) + "\n";
                TraceFile.write(m.c_str(), m.size());
                TraceFile.write(traceString.c_str(), traceString.size());
            }

            // at run time, just print the id
            std::string* s = new std::string(decstr(count_trace) + "\n");
            INS_InsertCall(BBL_InsTail(bbl), IPOINT_BEFORE, MAKE_AFUNPTR(docount), IARG_PTR, s, IARG_END);
        }
    }
}

/* ===================================================================== */

VOID Fini(INT32 code, VOID* v)
{
    const std::lock_guard< std::mutex > lock(writeMutex);
    TraceFile << "# eof" << std::endl;

    TraceFile.close();
}

/* ===================================================================== */
/* Main                                                                  */
/* ===================================================================== */

int main(int argc, char* argv[])
{
    std::string trace_header = std::string("#\n"
                                 "# Compressed Instruction Trace Generated By Pin\n"
                                 "#\n");

    if (PIN_Init(argc, argv))
    {
        return Usage();
    }

    TraceFile.open(KnobOutputFile.Value().c_str());
    TraceFile.write(trace_header.c_str(), trace_header.size());

    TRACE_AddInstrumentFunction(Trace, 0);
    PIN_AddFiniFunction(Fini, 0);

    // Never returns

    PIN_StartProgram();

    return 0;
}

